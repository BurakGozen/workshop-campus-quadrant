// Maak een functie toevoegen() die twee getallen bij elkaar optelt en het resultaat teruggeeft.
// Maak een functie aftrekken() die twee getallen van elkaar aftrekt en het resultaat teruggeeft.
// Maak een functie vermenigvuldigen() die twee getallen met elkaar vermenigvuldigt en het resultaat teruggeeft.
// Maak een functie delen() die twee getallen door elkaar deelt en het resultaat teruggeeft.

function toevoegen(getal1, getal2) {
  return getal1 + getal2;
}

function aftrekken(getal1, getal2) {
  return getal1 - getal2;
}

function vermenigvuldigen(getal1, getal2) {
  return getal1 * getal2;
}

function delen(getal1, getal2) {
  return getal1 / getal2;
}
